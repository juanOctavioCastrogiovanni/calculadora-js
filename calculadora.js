module.exports = {

    sumar: function (numero1, numero2){ 
    return numero1+numero2;  
},


    restar: function (numero1, numero2){
    return numero1-numero2;
},



    multiplicar: function (numero1, numero2){  
    return numero1*numero2;   
},


    dividir: function (numero1, numero2){
    return numero1/numero2;
}

}